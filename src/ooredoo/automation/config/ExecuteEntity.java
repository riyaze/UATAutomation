package ooredoo.automation.config;

public class ExecuteEntity {

	
	private String TS_ID;
	private String TS_Desc;
	private String TS_Fun_Name;
	private String TS_Exe_Flag;
	
	
	public String getTS_ID() {
		return TS_ID;
	}
	public void setTS_ID(String tS_ID) {
		TS_ID = tS_ID;
	}
	public String getTS_Desc() {
		return TS_Desc;
	}
	public void setTS_Desc(String tS_Desc) {
		TS_Desc = tS_Desc;
	}
	public String getTS_Fun_Name() {
		return TS_Fun_Name;
	}
	public void setTS_Fun_Name(String tS_Fun_Name) {
		TS_Fun_Name = tS_Fun_Name;
	}
	public String getTS_Exe_Flag() {
		return TS_Exe_Flag;
	}
	public void setTS_Exe_Flag(String tS_Exe_Flag) {
		TS_Exe_Flag = tS_Exe_Flag;
	}
	
	
	
	
}
